
puts "On va compter le nombre d'heures de travail à THP"
# dans cette ligne le #{} permet de calculé le temps de travail (55Oh) en affichant directement le résultat au sein d'une phrase
puts "Travail : #{10 * 5 * 11}"
# dans cette ligne le #{} permet aussi de calculé le temps de travail mais en mn  en affichant directement le résultat au sein d'une phrase
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

puts "Et en secondes ?"
# dans cette ligne le resultat de ce calcul
puts 10 * 5 * 11 * 60 * 60

# dans cette ligne une phrase
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

# dans cette ligne on interroge le programme directement pour savoir si le résultat es vrai ou faux
puts 3 + 2 < 5 - 7

# dans cette ligne  le #{} affiche le resultat de ce calcul (5) 
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
# dans cette ligne  le #{} affiche le resultat de ce calcul (-2)
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

puts "Ok, c'est faux alors !"

puts "C'est drôle ça, faisons-en plus :"

# dans cette phrase les #{} nous indique si l'affirmation es vrai ou fausse
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"